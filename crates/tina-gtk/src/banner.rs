// Reused as-is from the Slint frontend; pure-stdout, no UI deps.
use owo_colors::OwoColorize;

pub fn print_banner() {
    let ascii_art = r#"
     /\    \                  /\    \                  /\    \                  /\    \
    /::\    \                /::\    \                /::\____\                /::\    \
    \:::\    \               \:::\    \              /::::|   |               /::::\    \
     \:::\    \               \:::\    \            /:::::|   |              /::::::\    \
      \:::\    \               \:::\    \          /::::::|   |             /:::/\:::\    \
       \:::\    \               \:::\    \        /:::/|::|   |            /:::/__\:::\    \
       /::::\    \              /::::\    \      /:::/ |::|   |           /::::\   \:::\    \
      /::::::\    \    ____    /::::::\    \    /:::/  |::|   | _____    /::::::\   \:::\    \
     /:::/\:::\    \  /\   \  /:::/\:::\    \  /:::/   |::|   |/\    \  /:::/\:::\   \:::\    \
    /:::/  \:::\____\/::\   \/:::/  \:::\____\/:: /    |::|   /::\____\/:::/  \:::\   \:::\____\
   /:::/    \::/    /\:::\  /:::/    \::/    /\::/    /|::|  /:::/    /\::/    \:::\  /:::/    /
  /:::/    / \/____/  \:::\/:::/    / \/____/  \/____/ |::| /:::/    /  \/____/ \:::\/:::/    /
 /:::/    /            \::::::/    /                   |::|/:::/    /            \::::::/    /
/:::/    /              \::::/____/                    |::::::/    /              \::::/    /
\::/    /                \:::\    \                    |:::::/    /               /:::/    /
 \/____/                  \:::\    \                   |::::/    /               /:::/    /
                           \:::\    \                  /:::/    /               /:::/    /
                            \:::\____\                /:::/    /               /:::/    /
                             \::/    /                \::/    /                \::/    /
                              \/____/                  \/____/                  \/____/
"#;

    let (cut1, cut2, cut3) = (26, 52, 78);
    for line in ascii_art.lines() {
        if line.trim().is_empty() {
            continue;
        }
        if line.len() < cut3 {
            println!("{}", line);
            continue;
        }
        print!(
            "{}{}{}{}",
            (&line[0..cut1]).red().bold(),
            (&line[cut1..cut2]).green().bold(),
            (&line[cut2..cut3]).blue().bold(),
            (&line[cut3..]).magenta().bold(),
        );
        println!();
    }
    println!("{}", "-".to_string().repeat(97).yellow());
    println!();
}
