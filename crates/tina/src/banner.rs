use owo_colors::OwoColorize;
pub fn print_banner() {
    let ascii_art = r#"
     /\    \                  /\    \                  /\    \                  /\    \
    /::\    \                /::\    \                /::\____\                /::\    \
    \:::\    \               \:::\    \              /::::|   |               /::::\    \
     \:::\    \               \:::\    \            /:::::|   |              /::::::\    \
      \:::\    \               \:::\    \          /::::::|   |             /:::/\:::\    \
       \:::\    \               \:::\    \        /:::/|::|   |            /:::/__\:::\    \
       /::::\    \              /::::\    \      /:::/ |::|   |           /::::\   \:::\    \
      /::::::\    \    ____    /::::::\    \    /:::/  |::|   | _____    /::::::\   \:::\    \
     /:::/\:::\    \  /\   \  /:::/\:::\    \  /:::/   |::|   |/\    \  /:::/\:::\   \:::\    \
    /:::/  \:::\____\/::\   \/:::/  \:::\____\/:: /    |::|   /::\____\/:::/  \:::\   \:::\____\
   /:::/    \::/    /\:::\  /:::/    \::/    /\::/    /|::|  /:::/    /\::/    \:::\  /:::/    /
  /:::/    / \/____/  \:::\/:::/    / \/____/  \/____/ |::| /:::/    /  \/____/ \:::\/:::/    /
 /:::/    /            \::::::/    /                   |::|/:::/    /            \::::::/    /
/:::/    /              \::::/____/                    |::::::/    /              \::::/    /
\::/    /                \:::\    \                    |:::::/    /               /:::/    /
 \/____/                  \:::\    \                   |::::/    /               /:::/    /
                           \:::\    \                  /:::/    /               /:::/    /
                            \:::\____\                /:::/    /               /:::/    /
                             \::/    /                \::/    /                \::/    /
                              \/____/                  \/____/                  \/____/
"#;

    let cut1 = 26;
    let cut2 = 52;
    let cut3 = 78;

    for line in ascii_art.lines() {
        if line.trim().is_empty() {
            continue;
        }

        if line.len() < cut3 {
            println!("{}", line);
            continue;
        }

        let h_part = &line[0..cut1];
        let t_part = &line[cut1..cut2];
        let m_part = &line[cut2..cut3];
        let l_part = &line[cut3..];

        print!(
            "{}{}{}{}",
            h_part.red().bold(),
            t_part.green().bold(),
            m_part.blue().bold(),
            l_part.magenta().bold()
        );

        println!();
    }

    println!("{}", "-".to_string().repeat(97).yellow());
    println!();
    println!();
}
